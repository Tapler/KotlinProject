package JavaCodeToKotlin

import kotlin.math.*

class Kotlin_Project_51 {

    companion object {
        @JvmStatic
        fun main(args: Array<String>) {

            val x = 3.12
            val y = 3.5
            val z = 3.6
            println(Math.PI)
            println(abs(x))
            println(ceil(x))
            println(x.roundToInt())
            println(y.roundToInt())
            println(z.roundToInt())
            println(sin(30.0))
            println(2.0.pow(3.0))
            println(sqrt(9.0))
            println(max(y, z))
            println(Math.random())
        }
    }
}